    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String web = jTextField1.getText();
        try {
            this.fachadeConexion = new FacadeConexion(web);
            Desktop escritorio = null;
            if (Desktop.isDesktopSupported()) {
                escritorio = Desktop.getDesktop();
            }
            if (!(web.isEmpty())) {
                if (escritorio != null) {
                    escritorio.browse(this.fachadeConexion.getDireccion());
                } else {
                    JOptionPane.showMessageDialog(this, "Debe llenar campo");
                }
            }

        } catch (IOException io) {
            JOptionPane.showMessageDialog(null, "Error_inesperado");
        }


    }//GEN-LAST:event_jButton1ActionPerformed

